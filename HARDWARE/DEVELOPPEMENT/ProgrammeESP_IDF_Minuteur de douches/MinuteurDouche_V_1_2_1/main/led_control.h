#ifndef LED_CONTROL_H
#define LED_CONTROL_H

#include "driver/gpio.h"

/**-------------------------------------------------------------------------- --
   Constantes
-- -------------------------------------------------------------------------- */
#define LED_GPIO GPIO_NUM_2  /* GPIO utilisé pour piloter la LED */

/**-------------------------------------------------------------------------- --
   Fonctions publiques
-- -------------------------------------------------------------------------- */

/* -------------------------------------------------------------------------- --
   FUNCTION: led_init
   Initialise le GPIO de la LED en sortie et l’éteint par défaut
-- -------------------------------------------------------------------------- */
void led_init(void);

/* -------------------------------------------------------------------------- --
   FUNCTION: led_on
   Allume la LED en mettant le GPIO à 1
-- -------------------------------------------------------------------------- */
void led_on(void);

/* -------------------------------------------------------------------------- --
   FUNCTION: led_off
   Éteint la LED en mettant le GPIO à 0
-- -------------------------------------------------------------------------- */
void led_off(void);

/* -------------------------------------------------------------------------- --
   FUNCTION: led_toggle
   Inverse l'état actuel de la LED
-- -------------------------------------------------------------------------- */
void led_toggle(void);

/* -------------------------------------------------------------------------- --
   FUNCTION: led_get_state
   Retourne l’état actuel de la LED (0 = éteinte, 1 = allumée)
-- -------------------------------------------------------------------------- */
int led_get_state(void);

#endif // LED_CONTROL_H
